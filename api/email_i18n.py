def t(lang: str, key: str) -> str:
    """Return the translated string for key in lang, falling back to English."""
    return _STRINGS.get(lang, _STRINGS["en"]).get(key, _STRINGS["en"].get(key, key))


def pax_label(lang: str, adults: int, children_ages: list[int]) -> str:
    """Human-readable passenger summary in the given language."""
    s = t(lang, "adults") if adults != 1 else t(lang, "adult")
    parts = [f"{adults} {s}"]
    n = len(children_ages)
    if n == 1:
        age = children_ages[0]
        kind = t(lang, "infant") if age < 2 else t(lang, "child")
        parts.append(f"1 {kind} ({t(lang, 'age')} {age})")
    elif n > 1:
        ages_str = ", ".join(str(a) for a in children_ages)
        parts.append(f"{n} {t(lang, 'children')} ({t(lang, 'ages')} {ages_str})")
    return ", ".join(parts)
